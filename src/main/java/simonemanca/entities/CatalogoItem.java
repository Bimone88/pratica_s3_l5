package simonemanca.entities;

import jakarta.persistence.Column;
import jakarta.persistence.GeneratedValue;
import jakarta.persistence.GenerationType;
import jakarta.persistence.Id;
import jakarta.persistence.MappedSuperclass;

@MappedSuperclass
public abstract class CatalogoItem {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    protected Long id; // Modificato da String a Long per l'ID generato automaticamente

    @Column(name = "isbn", unique = true, nullable = false)
    protected String isbn;

    @Column(name = "titolo")
    protected String titolo;

    @Column(name = "anno_pubblicazione")
    protected int annoPubblicazione;

    @Column(name = "numero_pagine")
    protected int numeroPagine;

    // Costruttori
    public CatalogoItem() {
    }

    public CatalogoItem(String isbn, String titolo, int annoPubblicazione, int numeroPagine) {
        this.isbn = isbn;
        this.titolo = titolo;
        this.annoPubblicazione = annoPubblicazione;
        this.numeroPagine = numeroPagine;
    }

    // Getter e Setter
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getIsbn() {
        return isbn;
    }

    public void setIsbn(String isbn) {
        this.isbn = isbn;
    }

    public String getTitolo() {
        return titolo;
    }

    public void setTitolo(String titolo) {
        this.titolo = titolo;
    }

    public int getAnnoPubblicazione() {
        return annoPubblicazione;
    }

    public void setAnnoPubblicazione(int annoPubblicazione) {
        this.annoPubblicazione = annoPubblicazione;
    }

    public int getNumeroPagine() {
        return numeroPagine;
    }

    public void setNumeroPagine(int numeroPagine) {
        this.numeroPagine = numeroPagine;
    }
}

